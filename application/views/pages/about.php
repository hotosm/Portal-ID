	<div class="row">
	<div class="sevencol">
		<p>Soon after HOT got started there was discussion around the idea of pre-mapping before a disaster. Previously there have been meetings and discussions, but never really a project to attempt this. An opportunity arose to actually test this idea out. Here in Indonesia though we’ve begun work to answer the question “Can OpenStreetMap be used to collect exposure data?” A simple way to outline the program is by mapping buildings in OpenStreetMap. Can that data then be utilized in risk models?</p>
		<p>Beginning in April Kate Chapman and Jeff Haack began travelling to Indonesia to outline what a project to gather this sort of information would look like. We’ve been working with the Australia-Indonesia Facility for Disaster Reduction (AIFDR), the World Bank’s Global Facility for Disaster Reducation and Recovery (GFDRR), Badan Nasional untuk Penanggulangan Bencana (BNPB which is the Indonesia national government disaster response organization). When we were here the first time we meet with many perspective partners as well as gave a day and a half long workshop with some community facilitators from Australian Community Development and Civil Society Strengthening Scheme (ACCESS). ACCESS is a program which provides community facilitators that work in communities to assist in strengthen of governance. They assist in facilitation of community decision making and planning. These facilitators were already doing mapping within communities but it was more of a traditional participatory poverty mapping exercise. In our workshop we showed them how they could use OpenStreetMap to map their communities and then everyone would be able to use the data. After that first trip discussion about how we would further scale the mapping began and a three month pilot program was developed, which is what we’ve just begun recently.</p>
		<p>So over the next three months we are working in a five cities and eight rural areas. In the urban areas we are partnering with Universities and in the rural areas we are continuing to work with ACCESS.</p>
		<p>In the eight rural areas we are doing two day workshops. These will be followed up by one day workshops in the beginning of August. During the initial workshops we’ll be teaching data collection as well as collecting requirements for data usage. This will make it easy to make maps with cartography that is specific to Indonesia. When we return for the second workshop it will be to show off the modified tools and help with follow-up questions.</p>
		<p>In September 2011, HOT will be returning once more to delivery recommendations on the feasibility of extending these programs for all of Indonesia.</p>
